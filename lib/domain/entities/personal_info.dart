class PersonalInfo {
  final String phoneNumber;
  final String email;
  final int gender;
  final String country;
  final String currency;
  final String level;
  final String physical;

  const PersonalInfo({
    required this.phoneNumber,
    required this.email,
    required this.gender,
    required this.country,
    required this.currency,
    required this.level,
    required this.physical,
  });
}
